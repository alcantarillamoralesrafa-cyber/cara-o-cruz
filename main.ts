let moneda = 0
input.onGesture(Gesture.ThreeG, function () {
    moneda = randint(0, 1)
    if (moneda == 1) {
        basic.showLeds(`
            . . . . .
            . # . # .
            . . . . .
            # . . . #
            . # # # .
            `)
    } else {
        basic.showLeds(`
            . . # . .
            . . # . .
            # # # # #
            . . # . .
            . . # . .
            `)
    }
})
